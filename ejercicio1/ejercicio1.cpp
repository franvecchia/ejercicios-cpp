#include <iostream>
using namespace std;

int main () {
    int numero1, numero2;
    cout << "Ingrese un numero: ";
    cin >> numero1;
    cout << "Ingrese otro numero: ";
    cin >> numero2;

    cout << "Suma: " << numero1+numero2 << endl;
    cout << "Resta: " << numero1-numero2 << endl;
    cout << "Producto: " << numero1*numero2 << endl;

    return 0;
}